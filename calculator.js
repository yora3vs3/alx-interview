

// Create a function that takes two parameters, num1 and num2, and returns the result of the two numbers being added together.
function add(num1, num2) {
  return num1 + num2;
}

// Create a function that takes two parameters, num1 and num2, and returns the result of the two numbers being subtracted.
function subtract(num1, num2) {
  return num1 - num2;
}

// Create a function that takes two parameters, num1 and num2, and returns the result of the two numbers being multiplied together.
function multiply(num1, num2) {
  return num1 * num2;
}

